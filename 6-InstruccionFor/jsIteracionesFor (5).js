function mostrar()
{/**al presionar el botón repetir el pedido de número hasta que ingresemos el 9. */

    for (let index = 0; index = 1; index++) {
        repetir = parseInt(prompt("Ingrese un número ó 9 para finalizar"));
        if(repetir == 9)
        {
            alert("Ha ingresado 9, gracias!!!");
            break;
        }
    }
}//FIN DE LA FUNCIÓN